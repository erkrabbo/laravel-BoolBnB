     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'user_id' => 'required',
            'Poster' => 'required',
            'Title' => 'required',
            'Night_price' => 'required',
            'N_of_rooms' => 'required',
        ]);

        House::create($request->all());
        return redirect()->route('houses.index');
    }

